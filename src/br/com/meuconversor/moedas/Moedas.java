package br.com.meuconversor.moedas;

public class Moedas {
    private double dolar;
    private double real;
    private double pesoArgentino;
    private double pesoColombiano;

    public Moedas() {
        this.dolar = dolar;
        this.real = real;
        this.pesoArgentino = pesoArgentino;
        this.pesoColombiano = pesoColombiano;
    }

    public double getDolar() {
        return dolar;
    }

    public double getReal() {
        return real;
    }

    public double getPesoArgentino() {
        return pesoArgentino;
    }

    public double getPesoColombiano() {
        return pesoColombiano;
    }
}
